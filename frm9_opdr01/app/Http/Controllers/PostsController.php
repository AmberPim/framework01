    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $filenameWithExt = $request->file('image')->getClientOriginalName();
        $filename = pathinfo($filenameWithExt, PATHINFO_FILENAME);
        $extension = $request->file('image')->getClientOriginalExtension();
        $imagetostore = $filename . '_' . time() . '.' . $extension;
        $path = $request->file('image')->storeAs('public/images', $imagetostore);
        $request->image->move(public_path('images'),$imagetostore);

        $post = new Post;
        $post->post_title = $request->input('title');
        $post->post_category = $request->input('category');
        $post->image = $imagetostore;

        $post->save();

        return redirect('/createpost')->with('success', 'Post created');
    }

